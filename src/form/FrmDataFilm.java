    private void CariData(){
    try{
        st = cn.createStatement();
        rs = st.executeQuery("SELECT * FROM crudmovie WHERE " 
                             + cmbCari.getSelectedItem().toString() + 
                              " LIKE '%" + txtCari.getText() + "%'");
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Judul");
        model.addColumn("Sutradara");
        model.addColumn("Tahun");
        model.addColumn("Produksi");
        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        model.setRowCount(0);
        
        while (rs.next()){
           Object [] data = {
               rs.getString("no"),
               rs.getString("judul"),
               rs.getString("sutradara"),
               rs.getString("tahun"),
               rs.getString("produksi")
           };
           model.addRow(data);
           tblData.setModel(model);
        }   
        } catch (Exception e) {
        
        }
    
    }
    
