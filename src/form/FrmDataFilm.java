    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            if (txtNo.getText().equals("") || 
                txtJudul.getText().equals("") || 
               txtSutradara.getText().equals("") || txtTahun.getText().equals("") || txtProduksi.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "data tidak boleh kosong", "validasi data",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (btnSimpan.getText() == "Save" ) {
                String cek = "SELECT * FROM crudmovie WHERE no = '" + txtNo.getText() + "'";
                rs = st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null,"upsss... no sudah pernah terisi");
                } else {
                    String sql = "INSERT INTO crudmovie VALUES ('" + txtNo.getText() +
                    "','" + txtJudul.getText() + "','" + txtSutradara.getText()
                    + "','" + txtTahun.getText() + "','" + txtProduksi.getText() + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                    Bersih();
                    TampilData();
                }
            }else {
                //            AKSI UBAH DATA
                    String update = "UPDATE crudmovie SET judul = '" + txtJudul.getText() + 
                    "', sutradara = '" + txtSutradara.getText() + 
                    "', tahun = '" + txtTahun.getText() + 
                    "', Produksi = '" + txtProduksi.getText() + 
                    "' WHERE no = '" + txtNo.getText()+"'"; 
                    st.executeUpdate(update);
                    JOptionPane.showMessageDialog(null,"Data berhasil disimpan");
                    Bersih();
                    TampilData();
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (txtNo.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Silahkan Pilih data yang akan dihapus");

        } else { 
            int Jawab = JOptionPane.showConfirmDialog(null,"Data Ini Akan Dihapus, Yakin ??");
            if (Jawab == 0) {
                try{
                    st = cn.createStatement();
                    String sql = "DELETE FROM crudmovie where no ='" + txtNo.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                    TampilData();
                    Bersih();
                }catch (Exception e ){
                    JOptionPane.showMessageDialog(null, e);
                }

            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

